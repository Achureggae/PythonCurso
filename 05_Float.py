#  Numeros flotantes 

decimal = 2.5
decimal2 = 3.8782 

# Sumar numeros decimales e imprimir 

print(decimal+decimal2)

# metodo round, acotar decimales 

resultadoAcortado = decimal+decimal2

# primer parametro del metodo round es la variable y el segundo el numero de decimales que se desea
print(round(resultadoAcortado,2))