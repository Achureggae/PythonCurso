# Se pueden usar el numero de operadores que se desee
suma = 4+4+4+4+4+4+4
resta = 10-5-2
Multiplicacion = 3*4
division = 10/2

# Al tratarse de variable numerica tipo entero, no se concatenará la cadena, se hará la operacion de variables
print(suma + resta + Multiplicacion + division)

# Utilizar de comas para concatenar mutiples variables de tipo numerico 

print(suma,resta,Multiplicacion,division)


# Exponentes

# Para elevar a potencia se utiliza doble operador *
elevar = 10 ** 2
print(elevar)
elevado = 10 **3
print(elevado)

# Raiz cuadrada es elevado en fraccion

cuadrada = 4 ** (1/2)
print(cuadrada)


